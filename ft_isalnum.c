int	ft_isalnum(int c)
{
	if (c < 0 || c > 127)
		return (0);
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') ||
			(c >= '0' && c <= '9'))
		return (1);
	else
		return (0);
}
