int	ft_atoi(const char *str)
{
	int i;
	int j;
	int k;
	int neg;

	i = 0;
	k = 0;
	neg = 1;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
	|| str[i] == '\v' || str[i] == '\r' || str[i] == '\f')
		i++;
	if (str[i] == '+' && str[i - 1] != '-')
		i++;
	if (str[i] == '-' && str[i - 1] != '+')
	{
		neg = neg * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		j = str[i] - '0';
		k = k * 10 + j;
		i++;
	}
	return (k * neg);
}
