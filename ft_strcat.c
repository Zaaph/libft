#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int j;

	i = ft_strlen(s1) + 1;
	j = 0;
	while (s1[i])
	{
		s1[i] = s2[j];
		j++;
		i++;
	}
	return (s1);
}
