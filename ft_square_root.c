int		ft_square_root(int x)
{
	int i;

	i = 0;
	if (x == 1)
		return (1);
	while ((i * i) != x && i < 46341 && i < x)
	{
		i++;
		if ((i * i) == x)
			return (i);
	}
	return (-1);
}
